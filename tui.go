// Dashboard state management
// ---------------------------------------------------------------------------

func (d *MCPDashboard) handleInput(cmd string) {
	if cmd == "" {
		return
	}
	d.mu.Lock()
	defer d.mu.Unlock()

	tableRows := d.visibleTableRows()

	switch cmd {
	case "quit":
		d.stopOnce.Do(func() {
			if d.program != nil {
				d.program.Quit()
			}
		})
	case "up":
		d.selected = clamp(d.selected-1, 0, d.maxSelectable())
		d.expanded = false
	case "down":
		d.selected = clamp(d.selected+1, 0, d.maxSelectable())
		d.expanded = false
	case "home":
		d.selected = 0
		d.viewport = 0
		d.expanded = false
	case "end":
		d.selected = d.maxSelectable()
		d.expanded = false
	case "pagedown":
		d.selected = clamp(d.selected+tableRows/2, 0, d.maxSelectable())
		d.expanded = false
	case "pageup":
		d.selected = clamp(d.selected-tableRows/2, 0, d.maxSelectable())
		d.expanded = false
	case "toggle":
		if d.selected >= 0 {
			d.expanded = !d.expanded
		}
	case "pause":
		d.paused = !d.paused
	case "clear":
		d.events = d.events[:0]
		d.reqCount = 0
		d.okCount = 0
		d.errCount = 0
		d.unauth = 0
		d.selected = -1
		d.viewport = 0
		d.expanded = false
	case "filter":
		d.mode = modeFilter
		d.filter = ""
	case "escape":
		d.filter = ""
		d.mode = modeNormal
	}
	if d.selected >= 0 {
		d.viewport = clampViewport(d.selected, d.viewport, tableRows, len(d.events))
	}
}

func (d *MCPDashboard) visibleTableRows() int {
	// Approximate; will be refined by actual height in View
	return 20
}

func (d *MCPDashboard) maxSelectable() int {
	if len(d.events) == 0 {
		return 0
	}
	return len(d.events) - 1
}

func (d *MCPDashboard) recordEvent(evt MCPEvent) {
	d.mu.Lock()
	defer d.mu.Unlock()

	if d.paused {
		// Still count, but don't update selection/viewport
		d.reqCount++
		if evt.Status >= 400 || evt.Error != "" {
			d.errCount++
		} else {
			d.okCount++
		}
		if evt.Status == 401 {
			d.unauth++
		}
		if len(d.events) >= tuiLogCapacity {
			copy(d.events, d.events[1:])
			d.events[len(d.events)-1] = evt
		} else {
			d.events = append(d.events, evt)
		}
		return
	}

	if len(d.events) >= tuiLogCapacity {
		copy(d.events, d.events[1:])
		d.events[len(d.events)-1] = evt
		if d.selected > 0 {
			d.selected--
		}
		d.viewport--
	} else {
		d.events = append(d.events, evt)
	}
	d.reqCount++
	if evt.Status >= 400 || evt.Error != "" {
		d.errCount++
	} else {
		d.okCount++
	}
	if evt.Status == 401 {
		d.unauth++
	}
	if d.selected == -1 {
		d.selected = len(d.events) - 1
	}
	if d.viewport < 0 {
		d.viewport = 0
	}
	d.viewport = clampViewport(d.selected, d.viewport, d.visibleTableRows(), len(d.events))
}

