type MCPDashboardConfig struct {
	LocalURL           string
	RemoteURL          string
	RootDir            string
	ExposePath         string
	ExposeActive       bool
	AuthToken          string
	TokenAutoGenerated bool
	TailscaleHost      string
	TailscalePort      int
}

type MCPDashboard struct {
	localURL           string
	remoteURL          string
	rootDir            string
	exposePath         string
	exposeActive       bool
	authToken          string
	tokenAutoGenerated bool
	tailscaleHost      string
	tailscalePort      int

	startedAt time.Time

	mu sync.Mutex

	events   []MCPEvent
	reqCount int
	okCount  int
	errCount int
	unauth   int

	selected int
	expanded bool
	viewport int
	paused   bool
	mode     tuiMode
	filter   string
	program  *tea.Program
	stopOnce sync.Once
}

type mcpDashboardMsg struct {
	typ string
	evt MCPEvent
}

type tickMsg time.Time

type mcpDashboardModel struct {
	dashboard *MCPDashboard
	width     int
	height    int
}

func NewMCPDashboard(cfg MCPDashboardConfig) *MCPDashboard {
	remote := cfg.RemoteURL
	if remote == "" {
		remote = "not exposed"
	}
	return &MCPDashboard{
		localURL:           cfg.LocalURL,
		remoteURL:          remote,
		rootDir:            cfg.RootDir,
		exposePath:         cfg.ExposePath,
		exposeActive:       cfg.ExposeActive,
		authToken:          cfg.AuthToken,
		tokenAutoGenerated: cfg.TokenAutoGenerated,
		tailscaleHost:      cfg.TailscaleHost,
		tailscalePort:      cfg.TailscalePort,
		startedAt:          time.Now(),
		events:             make([]MCPEvent, 0, tuiLogCapacity),
		selected:           -1,
	}
}

func (d *MCPDashboard) Start(ctx context.Context, eventCh <-chan MCPEvent) {
	d.mu.Lock()
	if d.program != nil {
		d.mu.Unlock()
		return
	}
	d.startedAt = time.Now()
	d.mu.Unlock()

	model := &mcpDashboardModel{
		dashboard: d,
		width:     120,
		height:    40,
	}

	p := tea.NewProgram(model)
	d.mu.Lock()
	d.program = p
	d.mu.Unlock()

	go func() {
		if _, err := p.Run(); err != nil {
			log.Printf("dashboard: bubbletea exited with error: %v", err)
		}
	}()

	go func() {
		for {
			select {
			case <-ctx.Done():
				d.Stop()
				return
			case evt, ok := <-eventCh:
				if !ok {
					return
				}
				p.Send(mcpDashboardMsg{typ: "event", evt: evt})
			}
		}
	}()
}

func (d *MCPDashboard) Stop() {
	d.stopOnce.Do(func() {
		d.mu.Lock()
		p := d.program
		d.mu.Unlock()
		if p != nil {
			p.Quit()
		}
	})
}

// ---------------------------------------------------------------------------
// Bubbletea lifecycle
// ---------------------------------------------------------------------------

func (m mcpDashboardModel) Init() tea.Cmd {
	return tea.Tick(tuiRefreshMs, func(t time.Time) tea.Msg {
		return tickMsg(t)
	})
}

func (m mcpDashboardModel) Update(msg tea.Msg) (tea.Model, tea.Cmd) {
	d := m.dashboard

	switch msg := msg.(type) {
	case tea.WindowSizeMsg:
		m.width = msg.Width
		m.height = msg.Height
	case tea.KeyMsg:
		return m.handleKey(msg)
	case mcpDashboardMsg:
		if msg.typ == "event" {
			d.recordEvent(msg.evt)
		}
	case tickMsg:
		return m, tea.Tick(tuiRefreshMs, func(t time.Time) tea.Msg {
			return tickMsg(t)
		})
	}
	return m, nil
}

func (m mcpDashboardModel) handleKey(msg tea.KeyMsg) (tea.Model, tea.Cmd) {
	d := m.dashboard
	d.mu.Lock()
	mode := d.mode
	d.mu.Unlock()

	if mode == modeFilter {
		return m.handleFilterKey(msg)
	}

	switch msg.String() {
	case "q", "ctrl+c":
		return m, tea.Quit
	case "up", "k":
		d.handleInput("up")
	case "down", "j":
		d.handleInput("down")
	case "G":
		d.handleInput("end")
	case "g":
		d.handleInput("home")
	case "ctrl+d":
		d.handleInput("pagedown")
	case "ctrl+u":
		d.handleInput("pageup")
	case "enter":
		d.handleInput("toggle")
	case " ":
		d.handleInput("pause")
	case "c":
		d.handleInput("clear")
	case "/":
		d.handleInput("filter")
	case "escape":
		d.handleInput("escape")
	}
	return m, nil
}

func (m mcpDashboardModel) handleFilterKey(msg tea.KeyMsg) (tea.Model, tea.Cmd) {
	d := m.dashboard
	key := msg.String()

	switch key {
	case "escape":
		d.mu.Lock()
		d.mode = modeNormal
		d.filter = ""
		d.mu.Unlock()
	case "enter":
		d.mu.Lock()
		d.mode = modeNormal
		d.mu.Unlock()
	case "backspace":
		d.mu.Lock()
		if len(d.filter) > 0 {
			d.filter = d.filter[:len(d.filter)-1]
		}
		d.mu.Unlock()
	case "ctrl+u":
		d.mu.Lock()
		d.filter = ""
		d.mu.Unlock()
	default:
		if len(key) == 1 && key[0] >= 32 && key[0] < 127 {
			d.mu.Lock()
			d.filter += key
			d.mu.Unlock()
		}
	}
	return m, nil
}

func (m mcpDashboardModel) View() tea.View {
	d := m.dashboard
	state := d.snapshot()

	width := m.width
	if width <= 0 {
		width = 120
	}
	height := m.height
	if height <= 0 {
		height = 40
	}

	endpoint := state.RemoteURL
	if endpoint == "not exposed" {
		endpoint = state.LocalURL
	}

	var content string
	if state.ReqCount == 0 && state.Filter == "" {
		content = renderSetupScreen(width, height, state, endpoint)
	} else {
		content = renderLiveScreen(width, height, state, endpoint)
	}

	v := tea.NewView(content)
	v.AltScreen = true
	return v
}

