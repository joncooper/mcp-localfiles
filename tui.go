func toolShortName(tool string) string {
	if after, ok := strings.CutPrefix(tool, "tools.call:"); ok {
		return after
	}
	return tool
}

func formatSize(bytes int) string {
	if bytes <= 0 {
		return "-"
	}
	if bytes < 1024 {
		return fmt.Sprintf("%dB", bytes)
	}
	if bytes < 1024*1024 {
		return fmt.Sprintf("%.1fK", float64(bytes)/1024)
	}
	return fmt.Sprintf("%.1fM", float64(bytes)/(1024*1024))
}

func eventTime(evt MCPEvent) string {
	if evt.Timestamp.IsZero() {
		return "--:--:--"
	}
	return evt.Timestamp.Format("15:04:05")
}

func fallback(value string, replacement string) string {
	if strings.TrimSpace(value) == "" {
		return replacement
	}
	return value
}

func boolToWord(b bool) string {
	if b {
		return "enabled"
	}
	return "disabled"
}

func fitToWidth(value string, width int) string {
	if width <= 0 {
		return ""
	}
	value = truncateRunes(value, width)
	if runeWidth(value) < width {
		value += strings.Repeat(" ", width-runeWidth(value))
	}
	return value
}

func truncateRunes(value string, maxWidth int) string {
	if maxWidth <= 0 {
		return ""
	}
	runes := []rune(value)
	if len(runes) <= maxWidth {
		return value
	}
	if maxWidth <= 1 {
		return string(runes[:maxWidth])
	}
	return string(runes[:maxWidth-1]) + "…"
}

func runeWidth(value string) int {
	return utf8.RuneCountInString(value)
}

func padToHeight(content string, rows int) string {
	lines := strings.Split(content, "\n")
	for len(lines) < rows {
		lines = append(lines, "")
	}
	if len(lines) > rows {
		lines = lines[:rows]
	}
	return strings.Join(lines, "\n")
}

func wrapText(value string, maxW int) []string {
	if maxW <= 0 {
		return []string{""}
	}
	tokens := strings.Split(value, " ")
	if len(tokens) == 0 {
		return []string{""}
	}
	lines := make([]string, 0)
	current := tokens[0]
	for i := 1; i < len(tokens); i++ {
		n := tokens[i]
		if runeWidth(current)+1+runeWidth(n) > maxW {
			lines = append(lines, padLine(current, maxW))
			current = n
			continue
		}
		current = current + " " + n
	}
	lines = append(lines, padLine(current, maxW))
	return lines
}

func padLine(value string, width int) string {
	if runeWidth(value) < width {
		return value + strings.Repeat(" ", width-runeWidth(value))
	}
	if runeWidth(value) > width {
		return truncateRunes(value, width)
	}
	return value
}

func clamp(v, minV, maxV int) int {
	if v < minV {
		return minV
	}
	if v > maxV {
		return maxV
	}
	return v
}

func clampViewport(selected, current, window int, total int) int {
	if total <= window {
		return 0
	}
	if selected < 0 {
		return current
	}
	if selected < current {
		return selected
	}
	if selected >= current+window {
		return clamp(selected-window+1, 0, total-1)
	}
	return current
}
